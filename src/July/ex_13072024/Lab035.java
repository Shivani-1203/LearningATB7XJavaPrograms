public class Lab035 {
    public static void main(String[] args) {
        // Escape Chars
        char c = '\n'; // New line / Next Line
        char c1 = '\t'; // Add Tab
        char c2 = '\b'; // Backslash, delete 1 char
        char c3 = '\r'; // Backslash, delete 1 word
        System.out.println("Shivani" + c3 + "Paithara");
        System.out.println("Shivani" + c2 + "Paithara");
        System.out.println("Shivani" + c1 + "Paithara");
        System.out.println("Shivani" + c + "Paithara");


    }
}
